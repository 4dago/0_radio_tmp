int main(void) {

	// *************************** inicjalizacje ********************************
	DDRB |= (1 << PB5);				// LED testowy
//	DDRB |= ~(1 << PB0);			// PB0 jako wejście
	PORTB |= pinStrojenie;			// podciągnięte PB0
	PORTD |= pinMemo1;				// podciągnięte PD7
	PORTD |= pinMemo2;				// podciągnięte PD6


	// ******************* Inicjalizacja przycisków *****************************
	Tstrojenie.KPIN = &PINB;
	Tstrojenie.nr_guzika = 1;
	Tstrojenie.key_mask = pinStrojenie;
	Tstrojenie.wait_time_s = 2;
	Tstrojenie.kfun1 = &szukajStacji;
	Tstrojenie.kfun2 = NULL;

	Tmemo1.KPIN = &PIND;
	Tmemo1.nr_guzika = 2;
	Tmemo1.key_mask = pinMemo1;
	Tmemo1.wait_time_s = 2;
	Tmemo1.kfun1 = &WczytajMemo;
	Tmemo1.kfun2 = &ZapiszMemo;

	Tmemo2.KPIN = &PIND;
	Tmemo2.nr_guzika = 3;
	Tmemo2.key_mask = pinMemo2;
	Tmemo2.wait_time_s = 2;
	Tmemo2.kfun1 = &WczytajMemo;
	Tmemo2.kfun2 = &ZapiszMemo;

	// ******************* Przerwanie co 10ms z timera 2 ************************
	soft_timer_init();

	USART_Init(__UBRR);								// Inicjalizacja UART
	i2c_init(I2CBITRATE);								//inicjalizacja i2c
	si4703_init();
    SSD1306_init( SSD1306_SWITCHCAPVCC, REFRESH_MID );

	// *************************** przygotowania ********************************

    sei();												// globalne przerwainia

//	fm_setChannel(988);

//	pokaz_rejestry(0x00, 0x0f, si4703_registers);

	char rdsname[9] = "HELLO :)";
	char rdsrt[65] = "*** Radyjo *** MADZINKI ***";

	check_and_load();									// ustaw zapamiętaną stację lub domyślną


	//	// *************************** splash + włącz głos po 2' *************
    SSD1306_cls(); 										// czyszczenie zawertosci bufora
	SSD1306_drawKnownBitmap_P(0,0,RAZEM128x64,1); 		// rysuj splash
	SSD1306_display();										// przepisz bufor

	// czekaj 2'
	while (Timer1) {
		;
	}
	si4703_readRegisters();					// aktualizacja danych
	si4703_registers[POWERCFG] |= (1<<DMUTE);	// odblokuj głos
	si4703_writeRegisters2_7(); 				//Update
	fm_setVolume(15);


	// *************************** warstwa 1 ********************************

    SSD1306_cls(); 										// czyszczenie zawertosci bufora
	SSD1306_drawKnownBitmap_P(0,page0*8,RADIO16x16,1); 	// rysuj radio
    SSD1306_drawKnownBitmap_P(0,page6*8-1,GLOSNIK8x16,1);	// rysuj glosnik
    SSD1306_drawKnownBitmap_P(41,page6*8,RSSI19x16,1); 	// rysuj rssi

    SSD1306_display();										// przepisz bufor


	// *************************** warstwa 2 ********************************
	// **********************************************************************
	while (1) {


		key_press( &Tstrojenie );								// obsługa 1 guzika
		key_press( &Tmemo1 );									// obsługa 2 guzika
		key_press( &Tmemo2 );									// obsługa 2 guzika


		if (1 == radioFlagi.PS) {								// jeśli jest ps
			SSD1306_puts(20, page0 * 8, rdsname, 2, 1, 0);
			radioFlagi.PS = 0;
		}

		if (1 == radioFlagi.CT) {								// jeśli jest ct

//			SSD1306_put_int(110, page4 * 8, godziny + offsetutc / 2, 1, 1, 0);
//			SSD1306_put_int(110, page5 * 8, minuty, 1, 1, 0);
			radioFlagi.CT = 0;
		}

		if (!Timer3) {
			Timer3 = 3;								// co 50ms przesuwaj pasek

			static int i;

			if (1 == radioFlagi.RT) {

				if (i < -387) {
					i = 128;
					radioFlagi.RT = 0;
				}

				SSD1306_puts(i, page3 * 8, rdsrt, 1, 1, 0);
				SSD1306_refreshPages(page3, 3, 0, 127);
				i--;

			}
			// aktualizacja RDS - jaki czas będzie optymalny?
			fm_readRDS(rdsname, rdsrt);
		}


		if (!Timer4) {
			Timer4 = 20;								// co 200ms aktualizuj status
			//dograj informacje
			fm_getRssi();
			SSD1306_puts(11, page6 * 8, glosnik, 2, 1, 0);
			SSD1306_put_int(45, page7 * 8, rssi, 1, 0, 1);
			pokazHz(66, page6 * 8, fm_getChannel10x(), 2, 1, 0);
			SSD1306_display();
		}




		switch (uart_getc()) {
		case 'g':
			fm_seek(1);
			uart_putint(fm_getChannel10x(), 10);

			break;
		case 'd':
			fm_seek(0);
			uart_putint(fm_getChannel10x(), 10);

			break;
		case '+':
//				fm_readRDS(rdsdata, radiotext);
			fm_setVolume(glosnosc + 1);
			uart_putint(glosnosc, 10);

			break;
		case '-':
//				fm_readRDS(rdsdata, radiotext);
			fm_setVolume(glosnosc - 1);
			uart_putint(glosnosc, 10);
			break;
		case 'r':
//				uart_putint( fm_readRDS(rdsdata, radiotext), 10);
			uart_puts(rdsname);
			uart_puts("\n\r");
			uart_puts(rdsrt);
			uart_puts("\n\r");
//			uart_putint(godziny + offsetutc / 2, 10);
//			uart_puts(":");
//			uart_putint(minuty, 10);
//			uart_puts(" , offset: ");
//			uart_putint(offsetutc, 10);
//			uart_puts("\n\r");
			break;
		case 's':
			pokaz_rejestry(0x00, 0x0f, si4703_registers);
			break;
		}

	}

}

